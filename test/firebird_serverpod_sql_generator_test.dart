        ),
      );
    });

    test('omits random UUID database defaults in Firebird schema SQL', () {
      DatabaseDefinition buildDefinition(String defaultValue) {
        return DatabaseDefinition(
          moduleName: 'app',
          tables: [
            TableDefinition(
              name: 'uuid_table',
              dartName: 'UuidTable',
              schema: 'public',
              columns: [
                ColumnDefinition(
                  name: 'id',
                  columnType: ColumnType.bigint,
                  isNullable: false,
                  columnDefault: defaultIntSerial,
                ),
                ColumnDefinition(
                  name: 'publicId',
                  columnType: ColumnType.uuid,
                  isNullable: false,
                  columnDefault: defaultValue,
                ),
              ],
              foreignKeys: const [],
              indexes: const [],
            ),
          ],
          installedModules: const [],
          migrationApiVersion: 1,
        );
      }

      final randomSql = generator.generateDatabaseDefinitionSql(
        buildDefinition(defaultUuidValueRandom),
        installedModules: const [],
      );
      final randomV7Sql = generator.generateDatabaseDefinitionSql(
        buildDefinition(defaultUuidValueRandomV7),
        installedModules: const [],
      );

      expect(
        randomSql,
        contains('"PUBLICID" CHAR(36) CHARACTER SET ASCII NOT NULL'),
      );
      expect(
        randomV7Sql,
        contains('"PUBLICID" CHAR(36) CHARACTER SET ASCII NOT NULL'),
      );
      expect(
        randomSql,
        isNot(contains('"PUBLICID" CHAR(36) CHARACTER SET ASCII DEFAULT')),
      );
      expect(
        randomV7Sql,
        isNot(contains('"PUBLICID" CHAR(36) CHARACTER SET ASCII DEFAULT')),
      );
    });
