    test('generates alter table migration SQL for supported operations', () {
      final targetDefinition = DatabaseDefinition(
        moduleName: 'app',
        tables: [
          TableDefinition(
            name: 'task',
            dartName: 'Task',
            schema: 'public',
            columns: [
              ColumnDefinition(
                name: 'id',
                columnType: ColumnType.bigint,
                isNullable: false,
                columnDefault: defaultIntSerial,
              ),
              ColumnDefinition(
                name: 'title',
                columnType: ColumnType.text,
                isNullable: false,
